const NewStory: FC = () => {
  const navigate = useNavigate();
  const [blog, setBlog] = useState({ name: "", description: "" });
  const [loading, setLoading] = useState<boolean>(false);
  const [cid, setCid] = useState<string>("");
  const { address } = useAccount();
  const { config } = usePrepareContractWrite({
    addressOrName: '0x3007f9e235363eEa58F0ed684a261AAC8523A420',
    contractInterface: abi,
    functionName: 'safeMint',
    args: [address, `ipfs://${cid}/metadata.json`],
    overrides: {
      value: ethers.utils.parseEther('0.01'),
    },
  });
  const {
    isLoading: isContractWriteLoading,
    isSuccess: isContractWriteSuccess,
    write
  } = useContractWrite(config);

  //upload blog content and nft metadata to ipfs and mint
  const uploadFile = async (event: FormEvent<HTMLFormElement>) => {
    event.preventDefault();

    try {
      setLoading(true);
      const { data } = await axios.post("http://localhost:8000/uploadWeb3Storage", {
        blog
      });
      setCid(data?.cid);
      setLoading(false);
    } catch (error) {
      setLoading(false);
    }
  };

  useEffect(() => {
    if (cid) {
      console.log(cid);
      write?.();
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [cid]);

  console.log(cid.length)

  useEffect(() => {
    if (!isContractWriteLoading && isContractWriteSuccess) {
      navigate("/");
    }
  }, [isContractWriteLoading, isContractWriteSuccess, navigate])

  return (
    <>
      {loading ? (
        <Loading open={loading} />
      ) : (
        <div>
          <form onSubmit={uploadFile} className="writeForm">
            <div className="writeFormGroup">
              <input id="fileInput" type="file" style={{ display: "none" }} />
              <input
                className="writeInput"
                placeholder="Title"
                type="text"
                autoFocus={true}
                value={blog?.name}
                onChange={(e) => setBlog({ ...blog, name: e.target.value })}
              />
            </div>
            <div className="writeFormGroup">
              <textarea
                className="writeInput writeText"
                placeholder="Tell your story..."
                autoFocus={true}
                rows={5}
                value={blog?.description}
                onChange={(e) => setBlog({ ...blog, description: e.target.value })}
                style={{ marginTop: "1rem" }}
              />
            </div>
            <Button color="secondary" variant="contained" type="submit">
              Publish
            </Button>
          </form>
        </div>
      )}
    </>
  );
};

export default NewStory;
